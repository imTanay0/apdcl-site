import mongoose from 'mongoose'
import { isEmail } from 'validator'

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    validate: {
      validator: isEmail,
      message: 'Invalid email address',
    },
  },
  password: {
    type: String,
    required: true,
    unique: true,
  },
})

export default mongoose.model('User', userSchema)
